# ─── HypothesisPatternLibrary ─────────────────────────────────────────────────


class HypothesisPatternLibrary:
    """
    Registry of hypothesis patterns.

    Usage::

        library = HypothesisPatternLibrary()
        results = library.match(graph, evidence_objects, anchor_type="Incident")
        for r in results:
            if r.satisfied:
                # generate hypothesis from r.pattern
    """

    def __init__(self, extra_patterns: Optional[List[HypothesisPattern]] = None) -> None:
        self._patterns: List[HypothesisPattern] = list(BUILT_IN_PATTERNS)
        if extra_patterns:
            self._patterns.extend(extra_patterns)

    def register(self, pattern: HypothesisPattern) -> None:
        """Add a custom pattern to the library at runtime."""
        if any(p.pattern_id == pattern.pattern_id for p in self._patterns):
            raise ValueError(
                f"Pattern '{pattern.pattern_id}' already registered. "
                "Use a unique pattern_id."
            )
        self._patterns.append(pattern)

    def get(self, pattern_id: str) -> Optional[HypothesisPattern]:
        for p in self._patterns:
            if p.pattern_id == pattern_id:
                return p
        return None

    def all_patterns(self) -> List[HypothesisPattern]:
        return list(self._patterns)

    def match(
        self,
        graph: CanonGraph,
        evidence_objects: List[EvidenceObject],
        anchor_type: str,
    ) -> List[PatternMatchResult]:
        """
        Evaluate every registered pattern against *graph* and *evidence_objects*.

        Returns one PatternMatchResult per pattern regardless of satisfaction.
        Callers MUST check result.satisfied before generating a hypothesis.
        """
        results: List[PatternMatchResult] = []

        # Pre-build lookup sets for O(1) checks
        present_labels: Set[str] = {
            lbl for node in graph.nodes for lbl in node.labels
        }
        present_rel_types: Set[str] = {edge.type for edge in graph.edges}
        present_ev_types: Set[EvidenceType] = {ev.type for ev in evidence_objects}
        # Map label → node ids
        label_to_ids: Dict[str, List[str]] = {}
        for node in graph.nodes:
            for lbl in node.labels:
                label_to_ids.setdefault(lbl, []).append(node.neo4j_id)

        for pattern in self._patterns:
            if not pattern.anchor_matches(anchor_type):
                results.append(PatternMatchResult(
                    pattern=pattern,
                    satisfied=False,
                    unmet_node_labels=["anchor_type_mismatch"],
                ))
                continue

            unmet_nodes = [
                lbl for lbl in pattern.required_node_labels
                if lbl not in present_labels
            ]
            unmet_rels = [
                rt for rt in pattern.required_rel_types
                if rt not in present_rel_types
            ]
            unmet_ev = [
                et.value for et in pattern.required_evidence_types
                if et not in present_ev_types
            ]

            satisfied = not unmet_nodes and not unmet_rels and not unmet_ev

            # Collect matched node IDs for the template
            matched_node_ids: List[str] = []
            for lbl in pattern.required_node_labels:
                matched_node_ids.extend(label_to_ids.get(lbl, []))

            matched_rel_types = [
                rt for rt in pattern.required_rel_types
                if rt in present_rel_types
            ]
            matched_evidence_ids = [
                ev.evidence_id for ev in evidence_objects
                if ev.type in pattern.required_evidence_types
            ]

            # Build template vars from matched nodes
            template_vars: Dict[str, Any] = {
                "anchor_value": graph.anchor_primary_value,
            }
            for lbl in pattern.required_node_labels:
                ids = label_to_ids.get(lbl, [])
                if ids:
                    node = graph.get_node(ids[0])
                    if node:
                        key = f"{lbl.lower()}_value"
                        template_vars[key] = node.primary_value or ids[0]

            if satisfied:
                logger.debug("Pattern %s matched for anchor=%s", pattern.pattern_id, anchor_type)
            else:
                logger.debug(
                    "Pattern %s not satisfied: unmet_nodes=%s unmet_rels=%s unmet_ev=%s",
                    pattern.pattern_id, unmet_nodes, unmet_rels, unmet_ev
                )

            results.append(PatternMatchResult(
                pattern=pattern,
                satisfied=satisfied,
                matched_node_ids=matched_node_ids,
                matched_rel_types=matched_rel_types,
                matched_evidence_ids=matched_evidence_ids,
                unmet_node_labels=unmet_nodes,
                unmet_rel_types=unmet_rels,
                unmet_evidence_types=unmet_ev,
                template_vars=template_vars,
            ))

        satisfied_count = sum(1 for r in results if r.satisfied)
        logger.info(
            "Pattern matching: %d/%d patterns satisfied for anchor_type=%s",
            satisfied_count, len(results), anchor_type,
        )
        return results
