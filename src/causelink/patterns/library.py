"""
causelink/patterns/library.py

Hypothesis Pattern Library — Phase E.

Design:
  - HypothesisPattern describes a causal template rooted in the ontology.
  - Each pattern declares REQUIRED node labels, relationship types, and
    evidence types — all must be satisfied before the pattern fires.
  - HypothesisPatternLibrary.match() evaluates the CanonGraph + evidence
    against every registered pattern and returns PatternMatchResult list.
  - The HypothesisGeneratorAgent calls this library; LLM only fills
    description text (entity names/IDs), never the structural shape.

Pattern library is EXTENDABLE: call HypothesisPatternLibrary.register()
to add custom patterns without modifying this file.

Non-negotiable constraint:
  No hypothesis may be created without a matching pattern that has its
  required_node_labels, required_rel_types, and required_evidence_types
  all satisfied.  This is enforced by PatternMatchResult.satisfied.
"""

from __future__ import annotations

import logging
from dataclasses import dataclass, field
from typing import Any, Dict, FrozenSet, List, Optional, Set

from causelink.evidence.contracts import EvidenceObject, EvidenceType
from causelink.ontology.models import CanonGraph

logger = logging.getLogger(__name__)


# ─── HypothesisPattern ────────────────────────────────────────────────────────


@dataclass(frozen=True)
class HypothesisPattern:
    """
    A causal template grounded in the ontology schema.

    Fields:
        pattern_id              — stable ID used as Hypothesis.pattern_id
        name                    — human-readable name
        description_template    — template with {placeholders} for LLM fill-in
        required_node_labels    — ALL must be present in CanonGraph.nodes
        required_rel_types      — ALL must appear in at least one CanonGraph.edges
        required_evidence_types — ALL must match at least one EvidenceObject.type
        anchor_types            — pattern applicable when anchor is one of these
        confidence_prior        — base confidence assigned to generated hypotheses
        chain                   — which backtracking chain this pattern belongs to
    """

    pattern_id: str
    name: str
    description_template: str
    required_node_labels: FrozenSet[str]
    required_rel_types: FrozenSet[str]
    required_evidence_types: FrozenSet[EvidenceType]
    anchor_types: FrozenSet[str]
    confidence_prior: float = 0.40
    chain: str = "general"

    def anchor_matches(self, anchor_type: str) -> bool:
        return not self.anchor_types or anchor_type in self.anchor_types


# ─── PatternMatchResult ───────────────────────────────────────────────────────


@dataclass
class PatternMatchResult:
    """
    Result of matching a HypothesisPattern against the current InvestigationState.

    satisfied=True  → all requirements met; hypothesis may be generated.
    satisfied=False → requirements unmet; hypothesis MUST NOT be generated.
    """

    pattern: HypothesisPattern
    satisfied: bool
    matched_node_ids: List[str] = field(default_factory=list)
    matched_rel_types: List[str] = field(default_factory=list)
    matched_evidence_ids: List[str] = field(default_factory=list)
    unmet_node_labels: List[str] = field(default_factory=list)
    unmet_rel_types: List[str] = field(default_factory=list)
    unmet_evidence_types: List[str] = field(default_factory=list)
    # Additional context filled in by the matcher for template substitution
    template_vars: Dict[str, Any] = field(default_factory=dict)


